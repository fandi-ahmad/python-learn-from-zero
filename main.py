# dari celcius
celcius = float(input('masukan suhu celcius: '))
print(celcius, 'celcius')

reamur = (4/5) * celcius
print(reamur, 'reamur')

fahrenheit = ((9/5) * celcius) + 32
print(fahrenheit, 'fahrenheit')

kelvin = celcius + 273
print(kelvin, 'kelvin')

