# ===== OPERASI ARITMATIKA =====
a = 11
b = 2

# bagi
hasil = a / b
print(a, '/', b, '=', hasil)

# pangkat
hasil = a ** b
print(a, '**', b, '=', hasil)

# modulus (sisa)
hasil = a % b   # mengambil sisa dari a dibagi b
print(a, '%', b, '=', hasil)

# floor division (berapa kali)
hasil = a // b
print(a, '//', b, '=', hasil)


